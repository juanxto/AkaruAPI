-- Schema
-- CREATE USER akaru ... já cria no tablespace USERS

CREATE TABLE AKARU.TB_USUARIO (
    ID           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    FIREBASE_UID VARCHAR2(128) NOT NULL,
    NOME         VARCHAR2(150) NOT NULL,
    EMAIL        VARCHAR2(200) NOT NULL,
    LATITUDE     NUMBER(10,7),
    LONGITUDE    NUMBER(10,7),
    CIDADE       VARCHAR2(100),
    ESTADO       VARCHAR2(2),
    DT_CADASTRO  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UK_USUARIO_FIREBASE UNIQUE (FIREBASE_UID),
    CONSTRAINT UK_USUARIO_EMAIL UNIQUE (EMAIL)
);

CREATE TABLE AKARU.TB_PLANTIO (
    ID           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ID_USUARIO   NUMBER NOT NULL,
    ID_CULTURA   NUMBER NOT NULL,
    LATITUDE     NUMBER(10,7) NOT NULL,
    LONGITUDE    NUMBER(10,7) NOT NULL,
    CIDADE       VARCHAR2(100),
    ESTADO       VARCHAR2(2),
    DT_PLANTIO   TIMESTAMP NOT NULL,
    DETALHES     VARCHAR2(2000),
    DT_REGISTRO  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_PLANTIO_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES AKARU.TB_USUARIO (ID) ON DELETE CASCADE
);

CREATE TABLE AKARU.TB_PLANTIO_CULTURA (
    ID_PLANTIO NUMBER NOT NULL,
    ID_CULTURA NUMBER NOT NULL,
    CONSTRAINT PK_PLANTIO_CULTURA PRIMARY KEY (ID_PLANTIO, ID_CULTURA),
    CONSTRAINT FK_PC_PLANTIO FOREIGN KEY (ID_PLANTIO)
        REFERENCES AKARU.TB_PLANTIO (ID) ON DELETE CASCADE
);

CREATE TABLE AKARU.TB_HISTORICO_RECOMENDACAO (
    ID           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    ID_USUARIO   NUMBER NOT NULL,
    ID_CULTURA   NUMBER,
    NOME_CULTURA VARCHAR2(150),
    TEXTO        CLOB NOT NULL,
    SCORE        NUMBER(5,2),
    LATITUDE     NUMBER(10,7),
    LONGITUDE    NUMBER(10,7),
    DETALHES     VARCHAR2(2000),
    DADOS_CLIMA  VARCHAR2(4000),
    DT_GERACAO   TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_HIST_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES AKARU.TB_USUARIO (ID) ON DELETE CASCADE
);

CREATE INDEX IX_PLANTIO_USUARIO ON AKARU.TB_PLANTIO (ID_USUARIO);
CREATE INDEX IX_HIST_USUARIO ON AKARU.TB_HISTORICO_RECOMENDACAO (ID_USUARIO);
